# К этому моменту в messages уже есть результаты tool calls.

GENERATE_PROMPT = """\
Сформируй финальный ответ на вопрос пользователя на основе переписки выше.

Правила:
1. Если в переписке есть результаты поиска по базе знаний — используй
   ТОЛЬКО информацию из них. НЕ добавляй своих знаний.
   В конце обязательно перечисли источники (имена файлов .md):
   Источники: файл1.md, файл2.md
2. Если поиск не дал результатов — скажи:
   «В базе знаний нет информации по этому вопросу.»
3. Если использовались другие инструменты (дата, HUB) — ответь на основе
   их результатов.
4. Если инструменты не вызывались — ответь кратко по сути вопроса.
5. Используй markdown для форматирования. Будь кратким и точным.
"""
